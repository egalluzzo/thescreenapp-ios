#pragma mark - Managed object CRUD methods

- (void)addInterview {
    NSManagedObjectContext *context = [self.fetchedResultsController managedObjectContext];
    Interview *interview = (Interview *)[NSEntityDescription insertNewObjectForEntityForName:@"Interview"
                                                                      inManagedObjectContext:context];
    
    // Save the context.
    NSError *error = nil;
    if (![context save:&error]) {
        // FIXME: Handle errors properly.
        NSLog(@"Unresolved error %@, %@", error, [error userInfo]);
        abort();
    }
    
    [self.tableView selectRowAtIndexPath:[self.fetchedResultsController indexPathForObject:interview] animated:YES scrollPosition:UITableViewScrollPositionTop];
    self.detailViewController.interview = interview;
}

